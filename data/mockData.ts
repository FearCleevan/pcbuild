// Get pre-built series by ID or slug (e.g. `gaming` / `prebuilt-gaming`).
export const getPrebuiltSeries = (seriesId: string) => {
    const normalized = seriesId.toLowerCase();
    return preBuiltSeries[normalized as keyof typeof preBuiltSeries] ??
        preBuiltSeriesList.find((item) => item.id === normalized || item.slug === normalized);
};
